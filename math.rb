# we can use #{ }(embed ruby inside the string with ) in double quote only  , it doesnot work in single quote

puts '2+3 is #{2+3}'

puts "2 + 3 is #{2+3}"
puts "10 * 5 is #{10 *5}"
puts "0.4 * 12 is #{0.4*12}"
puts "10.0 / 3 is #{10.0/3}"
puts  "10 / 3 is #{10/3}"
puts "5 % 2 is #{5 %2}"
puts "7 % 7 is #{7%7}"

